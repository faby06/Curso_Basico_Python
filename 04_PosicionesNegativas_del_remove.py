#Posiciones Negativas
cel = ['Nokia','Huawei','Xiaomi']
print (cel[-3]) #aqui va empezar a imprimir de derecha a izquiera en este caso se va imprimir nokia

#Metodo del()
cel1 = ['Nokia','Huawei','Xiaomi']
del cel1[0] #usando este metodo elimina elemenentos de las listas con base a la posicion en este caso se va eliminar todo lo que este en la posicion 0 
print (cel1)

#Metodo remove
cel1 = ['Nokia','Huawei','Xiaomi']
cel1.remove('Huawei') #Usando eesta palabra eliminas de la lista original lo que escribas en los parentesis tienes que ponerlo exactamente igual lo que quieras eliminar
print (cel1) 
