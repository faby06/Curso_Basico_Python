#Eliminar elementos pop()
color=['Amarillo','Verde','Azul']
almacenar=color.pop(1)#Elimina elementos pero los puede almacenar en una variable 
print ("El color guardado es: ",(almacenar))
print(color)#aqui se va apreciar el color eliminado 

