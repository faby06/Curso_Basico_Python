#Ordenar elementos sort()
color=['Verde','Amarillo','Morado','Plata']
color.sort()#ordena alfabeticamente la lista
print("El orden alfabeticamente es:",(color))
color.sort(reverse=True)#Para que ponga el alfabeto al reves
print("El orden invertido es:",(color))

#Contar elementos usando len()
color=['Verde','Amarillo','Morado','Plata']
print ('Cuantos elementos son en la lista: ',len(color)) #len cuenta el numero de elementos o colores que hay en esta lista

