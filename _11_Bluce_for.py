#Uso Range
for a in range(5): #rango incrementa de uno en uno hasta que llega al 5  
   print('El resultado es:',a) 
for s in range(2,6): #Va empezar en el 2 y terminara hasta el 6 
   print('El resultado dos es:',s)
for r in range(0,12,2):#Inicia en el 0 y va incrementar de dos en dos hasta el 12 
    print('El resultado tres es:',r)
